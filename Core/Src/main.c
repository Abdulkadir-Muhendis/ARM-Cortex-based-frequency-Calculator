//CallBack Function
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_3) // If the interrupt source is Channel 3
	{
		if(Is_First_Captured == 0) //If the first calue is not captured
		{
			IC_Val_1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3); //Read the First value
			Is_First_Captured = 1; // Set the first captured as true
			
			//Now change the polarity to falling edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3, TIM_INPUTCHANNELPOLARITY_FALLING);
		}
		
		else if (Is_First_Captured == 1)  //if the first value is already captured
		{
			IC_Val_2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3);  //Read the second value
			__HAL_TIM_SET_COUNTER(htim, 0);  //Reset the counter 
			
			if(IC_Val_2 > IC_Val_1)
			{
				Difference = IC_Val_2 - IC_Val_1;
			}
			
			Is_First_Captured =0;  //Set it back to false
			//Set polarity to rising edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3, TIM_INPUTCHANNELPOLARITY_RISING);
		}
	}
}
